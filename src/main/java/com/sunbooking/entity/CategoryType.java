package com.sunbooking.entity;

/**
 * Enum representing different types of categories for content classification.
 * 
 * TOUR: Categories for tour packages
 * NEWS: Categories for news articles and travel tips
 * FOOD: Categories for food and dining experiences
 * PLACE: Categories for tourist destinations and attractions
 */
public enum CategoryType {
    /**
     * Tour categories - for travel tour packages
     */
    TOUR,

    /**
     * News categories - for travel news, tips, and guides
     */
    NEWS,

    /**
     * Food categories - for culinary experiences and restaurants
     */
    FOOD,

    /**
     * Place categories - for tourist destinations and attractions
     */
    PLACE
}
